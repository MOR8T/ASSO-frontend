export type AboutIntroBlock = { id: number; text: string; sort_order: number; style_variant: string };

type AboutTitleSectionProps = {
  /** Блоки описания с API; при отсутствии или пустом массиве используется захардкоженный текст */
  intro?: AboutIntroBlock[] | null;
};

export default function AboutTitleSection({ intro }: AboutTitleSectionProps) {
  const blocks =
    intro && intro.length > 0
      ? [...intro].sort((a, b) => a.sort_order - b.sort_order).map((b) => b.text)
      : FALLBACK_INTRO;

  return (
    <div className="max-min-h mt-[-102px] max-w-245 mx-auto text-[rgb(219,219,219)] flex flex-col justify-center gap-10 text-2xl font-extralight tracking-wider leading-none font-stretch-condensed">
      {blocks.map((text, i) => (
        <h3 key={i}>{text}</h3>
      ))}
    </div>
  );
}
